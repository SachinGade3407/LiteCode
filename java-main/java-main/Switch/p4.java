class Demo{

        public static void main(String[]args){

                int ch=65;

                switch(ch){


                        case 'A':
                                System.out.println("A");
				break;
                        case 66:
                                System.out.println("B");
				break;
                        case 'C':
                                System.out.println("67");
				break;
                        case 'D':
                                System.out.println("68");
				break;
                        case 'E':
                                System.out.println("69");
				break;
                        default :
                                System.out.println("No Match");
				break;
                }
                System.out.println("After switch");
        }
}


